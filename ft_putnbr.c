#include "ft_printf.h"

void	ft_putnbr(int n)
{
	long long nbr;
	
	nbr = n;
	if (nbr < 0)
	{
		nbr *= -1;
	   ft_putchar('-');	
	}
	if (nbr >= 10)
	{
		ft_putnbr(nbr /10);
	}
	ft_putchar((nbr % 10) + '0');
}
